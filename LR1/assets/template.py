def generate_classification_data(n_samples=1000, n_features=2,
                                  random_state=None, difficulty='medium'):
    """
    Generate synthetic binary classification dataset

    Parameters:
    -----------
    n_samples : int
        Number of samples
    n_features : int
        Number of features
    random_state : int
        Random seed
    difficulty : str
        'easy', 'medium', or 'hard'

    Returns:
    --------
    X, y : arrays
        Features and labels
    """
    if random_state is not None:
        np.random.seed(random_state)

    # Generate features
    X = np.random.randn(n_samples, n_features)

    # Generate labels based on difficulty
    if difficulty == 'easy':
        # Linearly separable
        weights = np.random.randn(n_features) * 3
        z = X @ weights + np.random.randn(n_samples) * 0.1
    elif difficulty == 'medium':
        # Moderately separable
        weights = np.random.randn(n_features) * 2
        z = X @ weights + np.random.randn(n_samples) * 1.0
    else:  # hard
        # Difficult to separate
        weights = np.random.randn(n_features)
        z = X @ weights + np.random.randn(n_samples) * 2.0

    y = (z > 0).astype(int)

    return X, y


