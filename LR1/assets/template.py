def gradient_descent(X, y, theta, alpha, iterations, lambda_reg=0, verbose=True):
    """
    Gradient Descent Optimization

    Update Rule:
    θ := θ - α * ∇J(θ)

    Parameters:
    -----------
    X : array, shape (m, n)
        Training data
    y : array, shape (m,)
        Labels
    theta : array, shape (n,)
        Initial parameters
    alpha : float
        Learning rate
    iterations : int
        Number of iterations
    lambda_reg : float
        Regularization parameter
    verbose : bool
        Print progress

    Returns:
    --------
    theta : array
        Optimized parameters
    cost_history : list
        Cost at each iteration
    """
    cost_history = []
    theta = theta.copy()

    for i in range(iterations):
        # Compute gradient
        gradient = compute_gradient(X, y, theta, lambda_reg)

        # Update parameters
        theta = theta - alpha * gradient

        # Compute cost
        cost = compute_cost(X, y, theta, lambda_reg)
        cost_history.append(cost)

        # Print progress
        if verbose and (i % 100 == 0 or i == iterations - 1):
            print(f"Iteration {i:4d}: Cost = {cost:.6f}")

    return theta, cost_history


def stochastic_gradient_descent(X, y, theta, alpha, epochs, lambda_reg=0,
                                batch_size=1, verbose=True):
    """
    Stochastic Gradient Descent (SGD) / Mini-batch GD

    Parameters:
    -----------
    X : array, shape (m, n)
        Training data
    y : array, shape (m,)
        Labels
    theta : array, shape (n,)
        Initial parameters
    alpha : float
        Learning rate
    epochs : int
        Number of epochs
    lambda_reg : float
        Regularization parameter
    batch_size : int
        Batch size (1 for SGD, m for batch GD)
    verbose : bool
        Print progress

    Returns:
    --------
    theta : array
        Optimized parameters
    cost_history : list
        Cost at each epoch
    """
    m = len(y)
    cost_history = []
    theta = theta.copy()

    for epoch in range(epochs):
        # Shuffle data
        indices = np.random.permutation(m)
        X_shuffled = X[indices]
        y_shuffled = y[indices]

        # Mini-batch updates
        for i in range(0, m, batch_size):
            X_batch = X_shuffled[i:i+batch_size]
            y_batch = y_shuffled[i:i+batch_size]

            # Compute gradient on batch
            gradient = compute_gradient(X_batch, y_batch, theta, lambda_reg)

            # Update parameters
            theta = theta - alpha * gradient

        # Compute cost on full dataset
        cost = compute_cost(X, y, theta, lambda_reg)
        cost_history.append(cost)

        if verbose and (epoch % 10 == 0 or epoch == epochs - 1):
            print(f"Epoch {epoch:4d}: Cost = {cost:.6f}")

    return theta, cost_history


def newton_method(X, y, theta, iterations=10, lambda_reg=0, verbose=True):
    """
    Newton's Method (Second-order optimization)

    Update Rule:
    θ := θ - H^(-1) * ∇J(θ)
    where H is the Hessian matrix

    Parameters:
    -----------
    X : array, shape (m, n)
        Training data
    y : array, shape (m,)
        Labels
    theta : array, shape (n,)
        Initial parameters
    iterations : int
        Number of iterations
    lambda_reg : float
        Regularization parameter
    verbose : bool
        Print progress

    Returns:
    --------
    theta : array
        Optimized parameters
    cost_history : list
        Cost at each iteration
    """
    m, n = X.shape
    cost_history = []
    theta = theta.copy()

    for i in range(iterations):
        # Compute predictions
        h = sigmoid(X @ theta)

        # Compute gradient
        gradient = compute_gradient(X, y, theta, lambda_reg)

        # Compute Hessian: H = 1/m * X^T * diag(h(1-h)) * X
        D = np.diag(h * (1 - h))
        H = (1/m) * (X.T @ D @ X)

        # Add regularization to Hessian
        if lambda_reg > 0:
            reg_matrix = (lambda_reg / m) * np.eye(n)
            reg_matrix[0, 0] = 0  # Don't regularize bias
            H += reg_matrix

        # Update parameters: θ := θ - H^(-1) * gradient
        try:
            theta = theta - np.linalg.solve(H, gradient)
        except np.linalg.LinAlgError:
            print("Warning: Singular Hessian matrix, using gradient descent step")
            theta = theta - 0.1 * gradient

        # Compute cost
        cost = compute_cost(X, y, theta, lambda_reg)
        cost_history.append(cost)

        if verbose:
            print(f"Iteration {i:2d}: Cost = {cost:.6f}")

    return theta, cost_history


