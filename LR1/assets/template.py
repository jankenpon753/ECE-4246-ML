def sigmoid(z):
    """
    Sigmoid activation function: σ(z) = 1 / (1 + e^(-z))

    Parameters:
    -----------
    z : array-like
        Linear combination of inputs

    Returns:
    --------
    Sigmoid activation values between 0 and 1
    """
    # Clip values to prevent overflow
    z = np.clip(z, -500, 500)
    return 1.0 / (1.0 + np.exp(-z))


def compute_cost(X, y, theta, lambda_reg=0):
    """
    Compute logistic regression cost function with optional regularization

    Cost Function:
    J(θ) = -1/m * Σ[y*log(h(x)) + (1-y)*log(1-h(x))] + λ/(2m) * Σθ²

    Parameters:
    -----------
    X : array, shape (m, n)
        Training examples
    y : array, shape (m,)
        True labels (0 or 1)
    theta : array, shape (n,)
        Model parameters
    lambda_reg : float
        Regularization parameter

    Returns:
    --------
    cost : float
        Cost value
    """
    m = len(y)

    # Compute predictions
    h = sigmoid(X @ theta)

    # Add small epsilon to prevent log(0)
    epsilon = 1e-7
    h = np.clip(h, epsilon, 1 - epsilon)

    # Compute cost
    cost = (-1/m) * np.sum(y * np.log(h) + (1 - y) * np.log(1 - h))

    # Add regularization term (excluding bias term theta[0])
    if lambda_reg > 0:
        reg_term = (lambda_reg / (2 * m)) * np.sum(theta[1:] ** 2)
        cost += reg_term

    return cost


def compute_gradient(X, y, theta, lambda_reg=0):
    """
    Compute gradient of cost function

    Gradient:
    ∂J/∂θ = 1/m * X^T * (h(x) - y) + λ/m * θ

    Parameters:
    -----------
    X : array, shape (m, n)
        Training examples
    y : array, shape (m,)
        True labels
    theta : array, shape (n,)
        Model parameters
    lambda_reg : float
        Regularization parameter

    Returns:
    --------
    gradient : array, shape (n,)
        Gradient vector
    """
    m = len(y)

    # Compute predictions
    h = sigmoid(X @ theta)

    # Compute gradient
    gradient = (1/m) * (X.T @ (h - y))

    # Add regularization (excluding bias term)
    if lambda_reg > 0:
        reg_gradient = (lambda_reg / m) * theta
        reg_gradient[0] = 0  # Don't regularize bias term
        gradient += reg_gradient

    return gradient


