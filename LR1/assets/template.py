class LogisticRegression:
    """
    Logistic Regression Model

    Parameters:
    -----------
    learning_rate : float
        Learning rate for gradient descent
    iterations : int
        Number of training iterations
    lambda_reg : float
        Regularization parameter (0 = no regularization)
    optimizer : str
        Optimization method ('gd', 'sgd', 'newton')
    batch_size : int
        Batch size for SGD
    verbose : bool
        Print training progress
    """

    def __init__(self, learning_rate=0.01, iterations=1000, lambda_reg=0,
                 optimizer='gd', batch_size=32, verbose=True):
        self.learning_rate = learning_rate
        self.iterations = iterations
        self.lambda_reg = lambda_reg
        self.optimizer = optimizer
        self.batch_size = batch_size
        self.verbose = verbose
        self.theta = None
        self.cost_history = []
        self.fitted = False

    def _add_intercept(self, X):
        """Add intercept term (bias) to feature matrix"""
        m = X.shape[0]
        return np.hstack([np.ones((m, 1)), X])

    def fit(self, X, y):
        """
        Train the logistic regression model

        Parameters:
        -----------
        X : array, shape (m, n)
            Training features
        y : array, shape (m,)
            Training labels (0 or 1)

        Returns:
        --------
        self : object
        """
        # Add intercept term
        X_with_intercept = self._add_intercept(X)

        # Initialize parameters
        n_features = X_with_intercept.shape[1]
        self.theta = np.zeros(n_features)

        # Choose optimization method
        if self.optimizer == 'gd':
            self.theta, self.cost_history = gradient_descent(
                X_with_intercept, y, self.theta,
                self.learning_rate, self.iterations,
                self.lambda_reg, self.verbose
            )
        elif self.optimizer == 'sgd':
            self.theta, self.cost_history = stochastic_gradient_descent(
                X_with_intercept, y, self.theta,
                self.learning_rate, self.iterations,
                self.lambda_reg, self.batch_size, self.verbose
            )
        elif self.optimizer == 'newton':
            self.theta, self.cost_history = newton_method(
                X_with_intercept, y, self.theta,
                self.iterations, self.lambda_reg, self.verbose
            )
        else:
            raise ValueError(f"Unknown optimizer: {self.optimizer}")

        self.fitted = True
        return self

    def predict_proba(self, X):
        """
        Predict probability estimates

        Parameters:
        -----------
        X : array, shape (m, n)
            Input features

        Returns:
        --------
        probabilities : array, shape (m,)
            Predicted probabilities
        """
        if not self.fitted:
            raise ValueError("Model must be fitted before making predictions")

        X_with_intercept = self._add_intercept(X)
        return sigmoid(X_with_intercept @ self.theta)

    def predict(self, X, threshold=0.5):
        """
        Predict class labels

        Parameters:
        -----------
        X : array, shape (m, n)
            Input features
        threshold : float
            Decision threshold

        Returns:
        --------
        predictions : array, shape (m,)
            Predicted class labels (0 or 1)
        """
        probabilities = self.predict_proba(X)
        return (probabilities >= threshold).astype(int)

    def score(self, X, y):
        """
        Calculate accuracy score

        Parameters:
        -----------
        X : array, shape (m, n)
            Input features
        y : array, shape (m,)
            True labels

        Returns:
        --------
        accuracy : float
            Accuracy score
        """
        predictions = self.predict(X)
        return np.mean(predictions == y)


